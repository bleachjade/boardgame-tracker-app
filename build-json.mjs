const HEADERS = {
  'User-Agent': 'Local-BGG-Catalog-Builder/1.0'
};

const token = process.env.BGG_API_TOKEN;
if (token) {
  console.log("🔒 Found BGG_API_TOKEN in environment. Attaching to headers...");
  HEADERS['Authorization'] = `Bearer ${token}`;
} else {
  console.warn("⚠️ No BGG_API_TOKEN found in .env file. Proceeding without authentication...");
}

async function buildDatabase() {
  console.log("🚀 Starting BGG XML to JSON conversion...");
  const gamesMap = new Map(); // Using a Map to automatically handle duplicate IDs
  
  try {
    const parser = new XMLParser({ ignoreAttributes: false, attributeNamePrefix: "@_" });

    for (let i = 0; i < CHUNKS.length; i++) {
      console.log(`   -> Fetching & converting chunk ${i + 1} of ${CHUNKS.length}...`);
      
      try {
        const res = await fetch(`https://boardgamegeek.com/xmlapi2/thing?stats=1&id=${CHUNKS[i]}`, { headers: HEADERS });
        
        if (!res.ok) {
          const errText = await res.text().catch(() => 'No text returned');
          throw new Error(`HTTP ${res.status} - ${errText.substring(0, 50)}...`);
        }

        const xmlData = await res.text();
        const parsed = parser.parse(xmlData);
        
        let items = parsed.items?.item;
        if (!items) continue;
        if (!Array.isArray(items)) items = [items];

        items.forEach((game) => {
          try {
            const bggId = String(game['@_id']);
            const names = Array.isArray(game.name) ? game.name : [game.name];
            const primaryName = names.find((n) => n['@_type'] === 'primary') || names[0];

            gamesMap.set(bggId, {
              bggId,
              name: primaryName?.['@_value'] || 'Unknown Game',
              year: game.yearpublished?.['@_value'] || '',
              image: game.image || game.thumbnail || '',
              minPlayers: game.minplayers?.['@_value'] || '0',
              maxPlayers: game.maxplayers?.['@_value'] || '99',
              playTime: game.playingtime?.['@_value'] || '0',
              rating: game.statistics?.ratings?.average?.['@_value'] || '0'
            });
          } catch (parseErr) {
            console.error(`⚠️ Failed to parse a specific game in chunk ${i + 1}:`, parseErr.message);
          }
        });

        await delay(2000); 
      } catch (chunkErr) {
        console.error(`❌ Network or API failed on chunk ${i + 1}:`, chunkErr.message);
      }
    }

    const uniqueGames = Array.from(gamesMap.values());

    try {
      fs.writeFileSync(OUTPUT_FILE, JSON.stringify(uniqueGames, null, 2));
      console.log(`\n🎉 DONE! Saved ${uniqueGames.length} unique games to ${OUTPUT_FILE}`);
    } catch (fsErr) {
      console.error(`❌ Failed to write the JSON file to ${OUTPUT_FILE}:`, fsErr.message);
    }

  } catch (fatalErr) {
    console.error("❌ Fatal error during database build:", fatalErr.message);
  }
}

buildDatabase().catch(err => {
  console.error("❌ Unhandled top-level error:", err.message);
});